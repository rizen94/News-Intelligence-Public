#!/bin/bash
# Update Docker Containers on NAS using sudo
# This script provides commands that can be run manually with sudo

NAS_HOST="192.168.93.100"
NAS_SSH_PORT="9222"
NAS_USER="Admin"
DOCKER_PATH="/usr/local/bin/docker"

echo "🐳 Docker Container Update Commands for NAS"
echo "==========================================="
echo ""
echo "⚠️  These commands require sudo. Run them manually on the NAS."
echo ""
echo "Step 1: SSH into NAS"
echo "--------------------"
echo "ssh -p $NAS_SSH_PORT $NAS_USER@$NAS_HOST"
echo ""
echo "Step 2: Check current containers"
echo "---------------------------------"
echo "sudo $DOCKER_PATH ps -a"
echo ""
echo "Step 3: Pull latest images"
echo "--------------------------"
echo "sudo $DOCKER_PATH pull postgres:15-alpine"
echo "sudo $DOCKER_PATH pull redis:7-alpine"
echo "sudo $DOCKER_PATH pull nginx:alpine"
echo ""
echo "Step 4: Find and update docker-compose (if exists)"
echo "---------------------------------------------------"
echo "find /volume1 -name docker-compose.yml 2>/dev/null"
echo "# Then:"
echo "cd /path/to/docker-compose.yml"
echo "sudo $DOCKER_PATH-compose pull"
echo "sudo $DOCKER_PATH-compose up -d"
echo ""
echo "Step 5: Or restart individual containers"
echo "----------------------------------------"
echo "sudo $DOCKER_PATH restart news-intelligence-postgres"
echo "sudo $DOCKER_PATH restart news-intelligence-redis"
echo "sudo $DOCKER_PATH restart news-intelligence-api"
echo "sudo $DOCKER_PATH restart news-intelligence-web"
echo ""
echo "Alternative: Fix permissions (one-time, then no sudo needed)"
echo "-----------------------------------------------------------"
echo "sudo usermod -aG docker $NAS_USER"
echo "# Then logout and login again"
echo "exit"
echo "ssh -p $NAS_SSH_PORT $NAS_USER@$NAS_HOST"
echo "# Now docker commands work without sudo"
echo ""

