#!/bin/bash
# Manual instructions and commands for updating Docker containers on NAS
# Since Docker requires permissions, this provides commands to run manually

NAS_HOST="192.168.93.100"
NAS_SSH_PORT="9222"
NAS_USER="Admin"

echo "🐳 Docker Container Update Guide for NAS"
echo "========================================"
echo ""
echo "Docker is installed at: /usr/local/bin/docker"
echo "Permission issue detected - Docker requires sudo or docker group membership"
echo ""
echo "OPTION 1: Run commands manually via SSH (Recommended)"
echo "----------------------------------------------------"
echo ""
echo "1. SSH into NAS:"
echo "   ssh -p $NAS_SSH_PORT $NAS_USER@$NAS_HOST"
echo ""
echo "2. Check current containers:"
echo "   /usr/local/bin/docker ps -a"
echo ""
echo "3. Find docker-compose.yml (if exists):"
echo "   find /volume1 -name docker-compose.yml 2>/dev/null"
echo ""
echo "4. Update containers (if docker-compose.yml exists):"
echo "   cd /path/to/docker-compose.yml"
echo "   /usr/local/bin/docker-compose pull"
echo "   /usr/local/bin/docker-compose up -d"
echo ""
echo "5. Or update individual containers:"
echo "   /usr/local/bin/docker pull postgres:15-alpine"
echo "   /usr/local/bin/docker pull redis:7-alpine"
echo "   /usr/local/bin/docker pull nginx:alpine"
echo ""
echo "OPTION 2: Use Container Station Web Interface"
echo "---------------------------------------------"
echo "1. Access NAS web interface: http://$NAS_HOST"
echo "2. Open Container Station"
echo "3. Navigate to containers"
echo "4. Update images and restart containers"
echo ""
echo "OPTION 3: Add user to docker group (one-time setup)"
echo "---------------------------------------------------"
echo "Run on NAS (requires admin access):"
echo "   sudo usermod -aG docker $NAS_USER"
echo "   # Then logout and login again"
echo ""
echo "Current Docker containers on NAS:"
echo "---------------------------------"
ssh -p "$NAS_SSH_PORT" "$NAS_USER@$NAS_HOST" "/usr/local/bin/docker ps -a --format 'table {{.Names}}\t{{.Image}}\t{{.Status}}' 2>&1" || echo "Permission denied - use one of the options above"
echo ""

